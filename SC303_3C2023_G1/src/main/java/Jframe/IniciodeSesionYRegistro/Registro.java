    // boton Registrar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = NombreRegistro.getText();
        String apellidos = jTextField2.getText();
        String nickname = jTextField3.getText();
        String password = jTextField4.getText();

        Usuario usuario = new Usuario(nombre, apellidos, nickname, password);
        registro.agregarUsuario(usuario);

        actualizarListaUsuarios();
    }//GEN-LAST:event_jButton1ActionPerformed
    
    // boton Inactivar por Nickname
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jTextField5.setVisible(true);
        jLabel5.setVisible(true);  
        String nickname = jTextField5.getText(); // Usamos el nuevo campo de texto para obtener el nickname
    
    if (registro.inactivarUsuario(nickname)) {
        jTextArea1.append("\nUsuario " + nickname + " ha sido inactivado.");
    } else {
        jTextArea1.append("\nNo se encontr√≥ el usuario con el nickname " + nickname + " o ya estaba inactivo.");
    }

    actualizarListaUsuarios();
    }//GEN-LAST:event_jButton2ActionPerformed

